    public void PanelPurchase(List<Purchase> list){
        int cont = 0;
        for(Purchase purchase: list){
            cont +=purchase.getValue_total().setScale(2, RoundingMode.DOWN).floatValue();
        }
        panelValueTotal.setText(String.valueOf(cont));
    }
    
