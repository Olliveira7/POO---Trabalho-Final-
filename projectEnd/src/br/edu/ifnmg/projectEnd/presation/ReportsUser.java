    private void btnPrurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrurchaseActionPerformed
        TableNull();
        RepositoryUser repositoryUser = new RepositoryUser();
        RepositoryPurchase repositoryPurchase = new RepositoryPurchase();
        if(!txtId.getText().isEmpty()){
            if(repositoryUser.CheckUserId(Integer.parseInt(txtId.getText())) == true){
                List<Purchase> list = new ArrayList<>();
                list = repositoryPurchase.OpenListId(Integer.parseInt(txtId.getText()), 2);
                UpdateTablePurchase(list);
                PanelPurchase(list);
            }else{
                JOptionPane.showMessageDialog(null, "Client does not exist!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Blank field!");
        }
    }//GEN-LAST:event_btnPrurchaseActionPerformed

