    private void btnSearcheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearcheActionPerformed
        Client clientTest = new Client();
        try{
            if(!txtSearcheId.getText().isEmpty()){
                client.setId(Integer.parseInt(txtSearcheId.getText()));
                clientTest = repository.CheckClientId(client);
                if(clientTest != null){
                    setClient(clientTest);
                    clientTest = repository.SeacheListTelephone(client);
                    for(String telephone : clientTest.getTelephones()){
                        this.client.addTelephone(telephone);
                        System.out.println(telephone);
                        UpdatePhone();
            }
                    getClient();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Required id field!");
            }
        }catch(Exception ex){
        
        }
    }//GEN-LAST:event_btnSearcheActionPerformed

