    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

            if(!txtName.getText().isEmpty() && !txtCnpj.getText().isEmpty()){
                if(repository.CheckProviderC(txtName.getText(), txtCnpj.getText()) == true){
                    if(JOptionPane.showConfirmDialog(null, "Do you really want to save changes?", "Confirm", JOptionPane.YES_NO_OPTION) == 0){
                        try{
                            setSaveProvider();
                            this.repository.Save(this.provider);
                            System.out.println(provider.getId());
                            if(provider.getTelephones().size() != 0)
                                this.repository.SaveTelephone(provider);
                            setNull();
                            setNullTelephone();
                            provider = new Provider();
                        }catch(ExceptionValidationError ex){
                            JOptionPane.showMessageDialog(null, ex.getMessage() + " Validation error!");
                        }
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Existing provider!");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Required components missing!!!");
            }
        setNull();
        setNullTelephone();
    }//GEN-LAST:event_btnSaveActionPerformed

