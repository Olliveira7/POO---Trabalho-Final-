    public void UpdateTable(List<Sale> list){
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Sale id");
        model.addColumn("Value");
        model.addColumn("Date");
        model.addColumn("User");
        
        for(Sale sale : list){
            Vector line = new Vector();
            line.add(sale.getId());
            line.add(sale.getValue_total());
            line.add(sale.getDate());
            line.add(sale.getUser().getUser());
            model.addRow(line);
        }
        
        tblSale.setModel(model);
    }
    
