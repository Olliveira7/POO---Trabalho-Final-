    private void btnSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaleActionPerformed
        TableNull();
        this.panelValueTotal.setText("");
        RepositorySale repositorySale = new RepositorySale();
        RepositoryClient repositoryClient = new RepositoryClient();
        if(!txtId.getText().isEmpty()){
            Client client = new Client();
            client.setId(Integer.parseInt(txtId.getText()));
            if(repositoryClient.CheckClientId(client) != null){
                List<Sale> list = new ArrayList<>();
                list = repositorySale.OpenListId(Integer.parseInt(txtId.getText()), 1);
                UpdateTable(list);
                Panel(list);
            }else{
                JOptionPane.showMessageDialog(null, "Client does not exist!");
            } 
        }else{
            JOptionPane.showMessageDialog(null, "Blank field!");
        }
    }//GEN-LAST:event_btnSaleActionPerformed


