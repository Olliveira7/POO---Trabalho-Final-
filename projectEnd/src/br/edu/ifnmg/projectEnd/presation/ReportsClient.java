    public void Panel(List<Sale> list){
        int cont = 0;
        for(Sale sale: list){
            cont +=sale.getValue_total().setScale(2, RoundingMode.DOWN).floatValue();
        }
        panelValueTotal.setText(String.valueOf(cont));
    }
    
