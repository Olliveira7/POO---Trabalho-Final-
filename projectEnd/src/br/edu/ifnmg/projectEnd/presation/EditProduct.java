    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        Product product = new Product();
        if(!txtId.getText().isEmpty() && !txtName.getText().isEmpty()){
            if(repository.CheckProduct(txtName.getText()) == false){
                product = repository.Open(Integer.parseInt(txtId.getText()));
                setProduct(product);
                JOptionPane.showMessageDialog(null, "Product exist!");
            }
        }
    }//GEN-LAST:event_btnCheckActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Product product = new Product();
        if((!txtId.getText().isEmpty() && !txtName.getText().isEmpty()) && (!this.txtPurchasePrice.getText().isEmpty() && !this.txtSalePrice.getText().isEmpty())){
            if(repository.CheckProduct(txtName.getText()) == false){
                product = repository.Open(Integer.parseInt(txtId.getText()));
                addProduct(product);
                
                JOptionPane.showMessageDialog(null, "Upload product!");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed


