    public void setProduct(Product product){
        this.txtDescription.setText(product.getDescription());
        this.txtPurchasePrice.setText(product.getPurchase_price().toString());
        this.txtSalePrice.setText(product.getSale_price().toString());
    }
    
    
    
    public void addProduct(Product product){
        product.setName(txtName.getText());
        product.setDescription(txtDescription.getText());
        product.setPurchase_unit(UnitPurchase.valueOf(cbxUnitPurchase.getSelectedItem().toString()));
        product.setSale_unit(UnitSale.valueOf(cbxUnitSale.getSelectedItem().toString()));
        product.setPurchase_price(BigDecimal.valueOf(Double.valueOf(txtPurchasePrice.getText())));
        product.setSale_price(BigDecimal.valueOf(Float.parseFloat(txtSalePrice.getText())));
        repository.Save(product);
    }
    
