    public void addProduct(Product product)throws Exception{
        Item item = new Item();
        
        item.setAmount(Integer.parseInt(txtAmount.getText()));
        item.setProduct(product);
        item.setValue(product.getSale_price());
        this.sale.addItem(item);
    }
    
