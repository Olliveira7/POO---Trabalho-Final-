    public void panelValueTotal(){
        float value = 0;
        for(Item item : this.sale.getItens()){
            value += item.getAmount() * item.getProduct().getSale_price().setScale(2, RoundingMode.DOWN).floatValue();
        }
        panelValueTotal.setText(String.valueOf(value));
    }
    
    public boolean check(Product product){
        RepositoryStock repo = new RepositoryStock();
        int value = 0;
        int amount = 0;
        amount = repo.CheckAmount(product.getId());
        Item et = new Item();
        for(Item item: sale.getItens()){
            if(item.getProduct() == product){
                value += item.getAmount();
                et = item;
            }
        }
        
        if(product.getSale_unit() == UnitSale.Unidade){
            if(value >= amount){
                sale.removeItem(et);
                return false;
            }else{
                return true;
            }
        }
        if(product.getSale_unit() == UnitSale.Conjunto){
            if(value*10 >= amount){
                sale.removeItem(et);
                return false;
            }else{
                return true;
            }
        }
        
        return false;
    }
    
    public void addStock(){
        RepositoryStock repository = new RepositoryStock();
        Stock stock = new Stock();
        for(Item item: sale.getItens()){
            stock.setProduct(item.getProduct());
            if(item.getProduct().getSale_unit() == UnitSale.Unidade){
                stock.setAmount(item.getAmount());
            }
            if(item.getProduct().getSale_unit() == UnitSale.Conjunto){
                stock.setAmount(item.getAmount() * 20);
            }
            if(item.getProduct().getSale_unit() == UnitSale.Caixa){
                stock.setAmount(item.getAmount() * 10);
            }
            repository.Save(stock, 0);
        }
    }
    
