    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        RepositoryProduct repositoryProduct = new RepositoryProduct();
        Product product = new Product();
        try{
            if(!txtProductName.getText().isEmpty() && !txtProductId.getText().isEmpty() && !txtAmount.getText().isEmpty()){
                if(repositoryProduct.CheckProductIdName(Integer.parseInt(txtProductId.getText()), txtProductName.getText()) == false){
                    if(repositoryProduct.CheckStatus(Integer.parseInt(txtProductId.getText())) == true){
                        try{
                           product = repositoryProduct.Open(Integer.parseInt(txtProductId.getText()));
                           addProduct(product);
                           if(check(product) == true){
                               setNull();
                               addTable();
                               panelValueTotal();
                           }else{
                               JOptionPane.showMessageDialog(null, "Falta no estoque ");
                           }
                           
                        }catch(Exception ex){}
                    }else{
                        JOptionPane.showMessageDialog(null, "Product disable ");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Product does not exist ");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Missing components");
            }
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        setNull();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        RepositoryClient repositoryClient = new RepositoryClient();
        RepositoryStock repositoryStock = new RepositoryStock();
        Client client = new Client();
        if(!txtClientId.getText().isEmpty()){
           client.setId(Integer.parseInt(txtClientId.getText()));
            if(repositoryClient.CheckClientId(client) != null){
                if(repositoryClient.CheckStatus(client.getId()) == true){
                    if(sale.getItens() != null){
                        RepositorySale repository = new RepositorySale();
                        client = repositoryClient.Open(Integer.parseInt(txtClientId.getText()));
                        sale.setClient(client);
                        sale.setValue_total(BigDecimal.valueOf(Double.valueOf(panelValueTotal.getText())));
                        sale.setUser(this.user);
                        if(repository.Save(this.sale) == true){
                            addStock();
                            repository.addItem(sale);
                            setNull();
                            JOptionPane.showMessageDialog(null, "Purchase save");
                            txtClientId.setText("");
                            panelValueTotal.setText("");
                            setNullTable();
                        sale = new Sale();
                        }
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Client Desable");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Client does not exist ");
            }
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed


